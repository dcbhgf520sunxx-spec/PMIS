const express = require('express')
const multer = require('multer')
const ctrl = require('../controllers/projectController')
const contractCtrl = require('../controllers/projectContractController')
const { MAX_ATTACHMENT_SIZE } = require('../services/projectContractAttachmentService')
const { fail } = require('../utils/response')
const router = express.Router()
const upload = multer({ storage: multer.memoryStorage(), limits: { fileSize: MAX_ATTACHMENT_SIZE } })
const uploadAttachment = (req, res, next) => upload.single('file')(req, res, (error) => {
  if (!error) return next()
  if (error.code === 'LIMIT_FILE_SIZE') return fail(res, 400, 400, '单个附件不能超过20MB')
  return fail(res, 400, 400, '附件上传失败')
})
router.get('/', ctrl.list)
router.get('/options', ctrl.options)
router.get('/:id/contract', contractCtrl.getByProject)
router.post('/:id/contract', contractCtrl.create)
router.put('/:id/contract', contractCtrl.update)
router.post('/:id/contract/attachments', uploadAttachment, contractCtrl.uploadAttachment)
router.get('/:id/contract/attachments/:attachmentId/download', contractCtrl.downloadAttachment)
router.delete('/:id/contract/attachments/:attachmentId', contractCtrl.deleteAttachment)
router.get('/:id/contract/stages/:stageId/payments', contractCtrl.listPayments)
router.post('/:id/contract/stages/:stageId/payments', contractCtrl.createPayment)
router.put('/:id/contract/payments/:paymentId', contractCtrl.updatePayment)
router.delete('/:id/contract/payments/:paymentId', contractCtrl.deletePayment)
router.get('/:id/history', ctrl.history)
router.get('/:id', ctrl.getById)
router.post('/', ctrl.create)
router.put('/:id', ctrl.update)
router.put('/:id/status', ctrl.toggleStatus)
router.delete('/:id', ctrl.remove)
module.exports = router
